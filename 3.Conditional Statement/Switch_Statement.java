public class Switch_Statement {
    public static void main(String[] args) {
        // about switch statement.
        // switch(variable){
        // case 1:
        // case 2:
        // case 3:
        // default:
        // }
        int number = 2;

        switch (number) {
            case 1:
                System.out.println("samosa");

                break;
            case 2:
                System.out.println("mango");
                break;

            case 3:
                System.out.println("burger");
                break;

            default:
                System.out.println("we wake up");
        }
    }

}
