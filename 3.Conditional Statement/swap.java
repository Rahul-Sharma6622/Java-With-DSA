public class swap {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println("before swaping : a = " +a + " and b  =" +b);
        int tem = a;
        a = b;
        b = tem;
        System.out.println("after swaping : a = " + a + " b =  " + b);

    }

}
